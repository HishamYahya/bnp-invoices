function SelectCustomerField({ customers, input }) {
  const classes = useStyles();
  const theme = useTheme();
  const [phone, setPhone] = React.useState(null);
  const [name, setName] = React.useState(null);
  const [creating, setCreating] = React.useState(false);

  const selectStyles = {
    input: base => ({
      ...base,
      color: theme.palette.text.primary,
      '& input': {
        font: 'inherit',
      },
    }),
  };

  const suggestions = customers.map(customer => ({
    label: customer.name,
    value: customer.phone,
    customer,
  }));

  function handleCreate(value) {
    setPhone(value);
    setCreating(true);
  }

  return (
    <div className={classes.root}>
      <NoSsr>
        <Dialog open={creating} onClose={() => setCreating(false)}>
          <DialogTitle>Create new customer</DialogTitle>
          <DialogContent>
            <TextField
              fullWidth
              label="Phone"
              type="tel"
              defaultValue={phone}
              onChange={e => setPhone(e.value)}
            />
            <TextField fullWidth label="Name" value={name} />
          </DialogContent>
          <DialogActions />
        </Dialog>
        <CreatableSelect
          isClearable
          label="Customer"
          placeholder="Search a customer's phone number..."
          classes={classes}
          styles={selectStyles}
          inputId="react-select-single"
          isLoading={creating}
          isDisabled={creating}
          onCreateOption={handleCreate}
          TextFieldProps={{}}
          options={suggestions}
          components={components}
          onChange={value => {
            input.onChange(value);
          }}
        />
      </NoSsr>
    </div>
  );
}

const mapStateToProps = state => ({ customers: state.customers });

export default connect(mapStateToProps)(SelectCustomerField);
